#[derive(Copy, Clone, Debug)]
enum Tool {
    Hands,
    Pickaxe(ToolTier),
    Shovel(ToolTier),
    Axe(ToolTier),
    Hoe(ToolTier),
    Sword(ToolTier),
    Shears,
    Bow,
    Crossbow,
    Trident,
}

#[derive(Copy, Clone, Debug)]
enum ToolTier {
    Wooden,
    Stone,
    Iron,
    Diamond,
    Netherite,
}

impl From<Item> for Tool {
    fn from(value: Item) -> Self {
        match value {
            Item::WoodenPickaxe => Tool::Pickaxe(ToolTier::Wooden),
            Item::StonePickaxe => Tool::Pickaxe(ToolTier::Stone),
            Item::IronPickaxe => Tool::Pickaxe(ToolTier::Iron),
            Item::DiamondPickaxe => Tool::Pickaxe(ToolTier::Diamond),
            Item::NetheritePickaxe => Tool::Pickaxe(ToolTier::Netherite),
            Item::WoodenAxe => Tool::Axe(ToolTier::Wooden),
            Item::StoneAxe => Tool::Axe(ToolTier::Stone),
            Item::IronAxe => Tool::Axe(ToolTier::Iron),
            Item::DiamondAxe => Tool::Axe(ToolTier::Diamond),
            Item::NetheriteAxe => Tool::Axe(ToolTier::Netherite),
            Item::WoodenShovel => Tool::Shovel(ToolTier::Wooden),
            Item::StoneShovel => Tool::Shovel(ToolTier::Stone),
            Item::IronShovel => Tool::Shovel(ToolTier::Iron),
            Item::DiamondShovel => Tool::Shovel(ToolTier::Diamond),
            Item::NetheriteShovel => Tool::Shovel(ToolTier::Netherite),
            Item::WoodenHoe => Tool::Hoe(ToolTier::Wooden),
            Item::StoneHoe => Tool::Hoe(ToolTier::Stone),
            Item::IronHoe => Tool::Hoe(ToolTier::Iron),
            Item::DiamondHoe => Tool::Hoe(ToolTier::Diamond),
            Item::NetheriteHoe => Tool::Hoe(ToolTier::Netherite),
            Item::WoodenSword => Tool::Sword(ToolTier::Wooden),
            Item::StoneSword => Tool::Sword(ToolTier::Stone),
            Item::IronSword => Tool::Sword(ToolTier::Iron),
            Item::DiamondSword => Tool::Sword(ToolTier::Diamond),
            Item::NetheriteSword => Tool::Sword(ToolTier::Netherite),
            Item::Shears => Tool::Shears,
            Item::Bow => Tool::Bow,
            Item::Crossbow => Tool::Crossbow,
            Item::Trident => Tool::Trident,
            _ => Tool::Hands,
        }
    }
}

impl ToolTier {
    pub fn can_break(self, required: ToolTier) -> bool {
        self as u32 >= required as u32
    }
}

pub fn block_to_item<R: Rng + ?Sized>(block: Block, tool: ItemStack, rng: &mut R) -> Vec<Item> {
    match tool {
        ItemStack::Empty => block_to_item_inner(block, Tool::Hands, false, 0, rng),
        ItemStack::Simple(s) => block_to_item_inner(block, s.item_type().into(), false, 0, rng),
        ItemStack::NbtItem(item) => {
            let nbt_tag = item.nbt();
            block_to_item_inner(block, item.item_type().into(), todo!(), todo!(), rng)
        }
    }
}

